function y=expense(x1,x2)
if((x1==1)&&(x2<24))  
        y=100*x2;
        if(y>600)
            y=600;
        end
else
    if((x1==1)&&(x2>=24)&&(x2<24*7))
           y=600*(x2/24)+100*(x2-x2/24*24);
           if(y>4200)
               y=4200;
           end
    end
end
 
if((x1==0)&&(x2<=0.5))
        y=0;
else
    if((x1==0)&&(x2>0.5)&&(x2<24))     
        y=((x2*60-30)/20)*100;
        if(y>300)
            y=300;
        end
    else
        if((x1==0)&&(x2>=24))
              y=(x2/24)*300+(x2-(x2/24)*24)*60/20*100;
              if((x2-(x2/24)*24)*60/20*100>300)
                   y=(x2/24)*300+300;
              end
        end
    end
end
